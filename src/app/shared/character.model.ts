export class Character {
  get _id(): string {
    return this._id;
  }
  set _id(value: string) {
    this._id = value;
  }
  get name(): string {
    return this.name;
  }
  set name(value: string) {
    this.name = value;
  }
  get role(): string {
    return this.role;
  }
  set role(value: string) {
    this.role = value;
  }
  get imagePath(): string {
    return this.imagePath;
  }
  set imagePath(value: string) {
    this.imagePath = value;
  }
}
